public class CompoundAssignments {
    public static void main(String[] args) {
        int a = 10;

        // Addition assignment
        a += 5; // Same as a = a + 5
        System.out.println("Result: " + a);

        // Subtraction assignment
        a -= 3; // Same as a = a - 3
        System.out.println("Result: " + a);

        // Multiplication assignment
        a *= 2; // Same as a = a * 2
        System.out.println("Result: " + a);

        // Division assignment
        a /= 4; // Same as a = a / 4
        System.out.println("Result: " + a);

        // Modulus assignment
        a %= 2; // Same as a = a % 2

        System.out.println("Result: " + a);
    }
}
